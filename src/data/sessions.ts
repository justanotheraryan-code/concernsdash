function addDaysISO(baseISO: string, days: number): string {
  const dt = new Date(`${baseISO}T00:00:00Z`);
  dt.setUTCDate(dt.getUTCDate() + days);
  return dt.toISOString().slice(0, 10);
}

function createTerm4Sessions(seed: Term4Seed): Session[] {
  const rows: Session[] = [];
  const ratingStart = seed.ratingBase;
  const ratingEnd = seed.ratingEnd ?? seed.ratingBase;
  const attendanceStart = seed.attendance;
  const attendanceEnd = seed.attendanceEnd ?? seed.attendance;
  const lockedZeroAttendance = attendanceStart === 0 && attendanceEnd === 0;
  const span = Math.max(1, seed.delivered - 1);

  for (let i = 0; i < seed.delivered; i += 1) {
    const progress = i / span;
    const ratingDrift = ratingStart + (ratingEnd - ratingStart) * progress;
    const attendanceDrift = attendanceStart + (attendanceEnd - attendanceStart) * progress;
    const ratingNoise = Math.sin((i + 1) * 1.47) * 0.05;
    const attendanceNoise = Math.cos((i + 1) * 1.23) * 0.8;
    const rating = Math.max(2.6, Math.min(4.9, ratingDrift + ratingNoise));
    const rawAttendance = lockedZeroAttendance ? 0 : attendanceDrift + attendanceNoise;
    const attendance = Math.max(0, Math.min(100, rawAttendance));
    const responses =
      attendance === 0
        ? 4 + (i % 3)
        : Math.max(14, Math.round(((seed.headcount * attendance) / 100) * 0.72));

    rows.push({
      id: `${seed.idPrefix}-${String(i + 1).padStart(3, "0")}`,
      dateISO: addDaysISO(seed.startDateISO, i),
      startTime: seed.startTime,
      program: "PGP",
      term: "Term 4",
      course: seed.course,
      professor: seed.professor,
      section: seed.section,
      topic: `${seed.topicBase} ${i + 1}`,
      status: "Delivered",
      ratingAvg: Number(rating.toFixed(2)),
      responses,
      attendancePct: Number(attendance.toFixed(1)),
      headcount: seed.headcount,
      notes: seed.notes,
    });
  }

  for (let i = seed.delivered; i < seed.planned; i += 1) {
    rows.push({
      id: `${seed.idPrefix}-${String(i + 1).padStart(3, "0")}`,
      dateISO: addDaysISO(seed.startDateISO, i),
      startTime: seed.startTime,
      program: "PGP",
      term: "Term 4",
      course: seed.course,
      professor: seed.professor,
      section: seed.section,
      topic: `${seed.topicBase} ${i + 1}`,
      status: "Planned",
      ratingAvg: null,
      responses: 0,
      attendancePct: null,
      headcount: seed.headcount,
      notes: "Planned for upcoming week.",
    });
  }

  return rows;
}

const term4Sessions: Session[] = term4Seed.flatMap(createTerm4Sessions);

export const sessions: Session[] = [...term3Sessions, ...term4Sessions];
